import os
from glob import glob

from setuptools import find_packages, setup

package_name = "fogros2"
setup(
    name=package_name,
    version="0.1.0",
    packages=find_packages(exclude=["test"]),
    data_files=[
        (
            "share/ament_index/resource_index/packages",
            [os.path.join("resource", package_name)],
        ),
        (os.path.join("share", package_name), ["package.xml"]),
        (
            os.path.join("share", package_name, "launch"),
            glob("launch/*.launch.py"),
        ),
    ],
    install_requires=["setuptools"],
    zip_safe=True,
    description="Run ROS 2 nodes on a remote SSH-reachable cloud machine.",
    long_description=(
        "Lightweight FogROS2-derived runtime for offloading ROS 2 nodes "
        "to a self-hosted cloud machine reachable over an existing private "
        "network (e.g. Tailscale)."
    ),
    license="Apache License, Version 2.0",
    tests_require=["pytest"],
)
